def get_all_study_sgfs() -> list[str]:
    """返回所有打谱用 SGF 文本列表"""
    return [sgf for _, sgf in STUDY_SGF_GAMES]

def get_study_game_info() -> list[dict]:
    """返回打谱棋谱的基本信息列表"""
    return [
        {
            "name": name,
            "move_count": _count_moves(sgf),
            "key_moves": _find_key_moves(sgf),
        }
        for name, sgf in STUDY_SGF_GAMES
    ]

def _count_moves(sgf: str) -> int:
    """快速统计 SGF 中的走法数"""
    import re
    return len(re.findall(r';(?:B|W)\[', sgf))

def _find_key_moves(sgf: str) -> list[str]:
    """提取 SGF 中标注的关键手"""
    import re
    # 找 N[xxx] 节点
    return re.findall(r'N\[([^\]]+)\]', sgf)