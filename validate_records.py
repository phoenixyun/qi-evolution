def validate_game(record):
    """使用GoEngine验证棋谱每一手，返回 (is_valid, error_msg, valid_up_to)"""
    engine = GoEngine(size=9)
    moves = record["moves"]
    name = record["name"]
    
    for i, (r, c) in enumerate(moves):
        color_name = "黑" if i % 2 == 0 else "白"
        move_num = i + 1
        
        if not (0 <= r < 9 and 0 <= c < 9):
            return False, f"第{move_num}手({color_name})坐标({r},{c})超出棋盘!", i
        
        if not engine.is_valid_move(r, c):
            return False, f"第{move_num}手({color_name})({r},{c})非法!", i
        
        engine.make_move(r, c)
    
    return True, "", len(moves)


def main():
    print("=" * 60)
    print("棋谱验证报告")
    print("=" * 60)
    
    all_pass = True
    
    for record in GAME_RECORDS:
        name = record["name"]
        pid = record["id"]
        players = record["players"]
        moves_count = len(record["moves"])
        
        ok, msg, valid_up_to = validate_game(record)
        
        if ok:
            print(f"✅ [{pid:2d}] {name:12s} | {players['黑']} vs {players['白']} | {moves_count}手 全部合法")
        else:
            all_pass = False
            print(f"❌ [{pid:2d}] {name:12s} | 仅{valid_up_to}手合法 | 错误: {msg}")
    
    print("=" * 60)
    
    if all_pass:
        print("🎉 全部棋谱验证通过！")
    else:
        print(f"⚠️ 部分棋谱需要修正")
    
    # 输出统计信息
    print()
    print("棋谱列表：")
    for record in GAME_RECORDS:
        print(f"  {record['id']}. {record['name']} ({record['year']}) — {record['players']['黑']} vs {record['players']['白']}")


if __name__ == "__main__":
    main()
